    private void populateComboBoxes() {
        int month = CommonUtils.getFieldFromDate(new Date(), Calendar.MONTH) + 1;
        CommonUtils.clear(cmbMonth1);
        CommonUtils.clear(cmbMonth2);
        CommonUtils.clear(cmbDay1);
        CommonUtils.clear(cmbDay2);
        for (int i = month; i <= 12; i++) {
            CommonUtils.populate(cmbMonth1, Arrays.asList(i));
            CommonUtils.populate(cmbMonth2, Arrays.asList(i));
        }
        for (int i = 1; i <= 31; i++) {
            CommonUtils.populate(cmbDay1, Arrays.asList(i));
            CommonUtils.populate(cmbDay2, Arrays.asList(i));
        }
    }

