    private void btnSetVacationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetVacationActionPerformed
        int year = CommonUtils.getFieldFromDate(new Date(), Calendar.YEAR);
        int month1 = CommonUtils.getSelection(cmbMonth1);
        int month2 = CommonUtils.getSelection(cmbMonth2);
        int day1 = CommonUtils.getSelection(cmbDay1);
        int day2 = CommonUtils.getSelection(cmbDay2);
        Date startDate = CommonUtils.parseDate(year, month1, day1, 0, 0);
        Date endDate = CommonUtils.parseDate(year, month2, day2, 0, 0);
        if (endDate.compareTo(startDate) < 0) {
            CommonUtils.showMessage("The end date cannot come before the start date");
        } else {
            UserAccount user = LoginService.getInstance().getCurrentAccount();
            user.setVacationStartDate(startDate);
            user.setVacationEndDate(endDate);
            user.setVacationDetails(txtVacationReason.getText());
            CommonUtils.showMessage("Vacation Range saved successfully");
            LoginService.getInstance().getCurrentAccount().getAuditTrail().add(
                    new AuditItem(new Date(), "Set vacation range: " + startDate + " to " + endDate)
            );
        }
    }//GEN-LAST:event_btnSetVacationActionPerformed


