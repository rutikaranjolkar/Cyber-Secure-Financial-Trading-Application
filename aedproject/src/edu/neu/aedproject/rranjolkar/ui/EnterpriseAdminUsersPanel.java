    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        Organization organization = CommonUtils.getSelection(cmbOrganizations);
        if (organization == null) {
            CommonUtils.showMessage("Please select an organization");
        } else {
            String name = txtName.getText().trim();
            String username = txtUsername.getText().trim();
            String password = new String(txtPassword.getPassword()).trim();
            String totpKey = txtTotpKey.getText().trim();
            if (name == null || name.isEmpty()) {
                CommonUtils.showMessage("Please enter a valid full name!");
            } else if (username == null || username.trim().isEmpty()) {
                CommonUtils.showMessage("Please enter a valid username!");
            } else if (password == null || password.trim().isEmpty()) {
                CommonUtils.showMessage("Please enter a valid password!");
            } else if (totpKey == null || totpKey.isEmpty()) {
                CommonUtils.showMessage("Please enter a valid TOTP key!");
            } else {
                boolean exists = LoginService.getInstance().userExists(username);
                if (exists) {
                    CommonUtils.showMessage("The username is already in use.");
                } else {
                    Employee employee = new Employee();
                    employee.setName(txtName.getText());
                    UserAccount user = new UserAccount();
                    user.setUsername(username);
                    user.setPassword(password);
                    user.setTotpKey(totpKey);
                    Role role = CommonUtils.getSelection(cmbRoles);
                    user.setRole(role);

                    txtName.setText("");
                    txtPassword.setText("");
                    txtUsername.setText("");
                    txtTotpKey.setText("");

                    organization.getUserAccountDirectory().getUserAccounts().add(user);
                    LoginService.getInstance().getCurrentAccount().getAuditTrail().add(
                            new AuditItem(new Date(), "Added user: " + user.getUsername())
                    );
                    CommonUtils.showMessage("Added user successfully!");
                }
            }
        }
        populateObjects();
    }//GEN-LAST:event_btnAddActionPerformed

