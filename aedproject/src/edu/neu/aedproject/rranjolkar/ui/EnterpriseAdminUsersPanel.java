    private void populateObjects() {
        Organization organization = CommonUtils.getSelection(cmbOrganizations);
        if (organization != null) {
            CommonUtils.clear(cmbRoles);
            CommonUtils.populate(cmbRoles, organization.getRoles());
        }
    }

    private void populateOrganizations() {
        Enterprise enterprise = LoginService.getInstance().getCurrentEnterprise();
        CommonUtils.clear(cmbOrganizations);
        List<Organization> orgs = enterprise.getOrganizationDirectory().getOrganizations();
        CommonUtils.populate(cmbOrganizations, orgs);
    }

